package demo;

public class DataTypes {

	//main()method --> starting/execution point of the program.
	public static void main(String[] args) {
		
		//Primitive data types: int, double, char and boolean
		// Non-Primitive data types: String.
		
		// 1. int
		int i = 20;
		 i = 10;
		 int j = 30;
		 System.out.println(j);
		 int k = 40;
		 int m = -1;
		 System.out.println(m);
		 System.out.println(k);
		 System.out.println("Adding of J and K : " + (j+k));
		 
		 // 2. double
		 double d =12.11;
		 double d1 = 33.33;
		 System.out.println(d1);
		 double d2 = 100;  // 100.0
		 System.out.println(d2);
		 
		 // 3.char:  only single digit value, should be written within single quote.
		 char c = 'a';
		 char c1 = 'A';
		 char c2 = '$';
		 System.out.println(c1);
		 
		 // 4.boolean:
		 boolean b1 = true;
		 boolean b2 = false;
		 
		 System.out.println(b2);
		 
		 //5. String - is class, not a data types
		 String s = "Hello World";
		 String s1 = "Java";
		 String s3 = "10000";
		 String s4 = "12.55";
		 System.out.println(s3);
		 
		 
		 
		 
		 
		 
		 
		 
		 
		 

	}

}
