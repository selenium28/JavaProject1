package demo;

public class ForLoopConcept {

	public static void main(String[] args) {

		//i++ means i = i+1
		//1 to 10 print
		//Initialization, condition, Increment
		for(int i=1; i<=10; i++) {
			System.out.println(i);

		}

		//j-- means j = j-1
		//1 to 10 print
		//Initialization, condition, Decrement
		for(int j=10; j>=1; j--) {
			System.out.println(j);

		}

	}

}
